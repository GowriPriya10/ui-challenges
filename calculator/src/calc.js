const Operations = {
    '+' : (a, b) => {
        return a + b;
    },

    '-' : (a, b) => {
        return a - b;
    },

    '*' : (a, b) => {
        return a * b;
    },

    '/' : (a, b) => {
        return a / b;
    }, 

    '%' : (a, b) => {
        return a % b;
    }
}

export default Operations;